package simulator.io;

public class ScenarioLoadingException extends Exception {

  public ScenarioLoadingException() {
    super();
  }

  public ScenarioLoadingException(String message, Throwable cause) {
    super(message, cause);
  }

  public ScenarioLoadingException(String message) {
    super(message);
  }

  public ScenarioLoadingException(Throwable cause) {
    super(cause);
  }


}
