package alevos;

public class IllegalSemanticsException extends Exception {

  public IllegalSemanticsException() {
    super();

  }

  public IllegalSemanticsException(String message, Throwable cause) {
    super(message, cause);
  }

  public IllegalSemanticsException(String message) {
    super(message);
  }

  public IllegalSemanticsException(Throwable cause) {
    super(cause);
  
  }

}
